"""
4. Implemente un algoritmo en Python para calcular el perímetro de un rectángulo, conociendo su base y 
altura. Los datos se deben almacenar en variables, y el resultado se debe mostrar en pantalla.
"""

base = int(input("Ingrese la base del rectángulo:  "))
altura = int(input("Ingrese la altura del rectángulo: "))

perimetro =  2 * (base + altura)

print(F"El perimetro del rectángulo es {perimetro}")

"""
5. Implemente un algoritmo en Python para calcular el área de un rectángulo, conociendo su base y altura. 	
Los datos se deben almacenar en variables, y el resultado se debe mostrar en pantalla.
"""

area = base * altura
print(f"El área del rectangulo es: {area}")